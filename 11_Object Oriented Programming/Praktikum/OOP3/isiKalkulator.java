package OOP3;

public class isiKalkulator {
    protected static float penjumlahan(float x, float y){
        return x+y;
    }
    protected static float pengurangan(float x, float y) {
        return x - y;
    }
    protected static float perkalian(float x, float y) {
        return x * y;
    }
    protected static float pembagian(float x, float y) {
        return x / y;
    }
}
